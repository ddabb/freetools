// 读取文件
function readFiles() {
  const files = fs.readdirSync(SOURCE_DIR)
    .filter(f => f.endsWith('.md'))
    .map(filename => {
      const filePath = path.join(SOURCE_DIR, filename);
      const content = fs.readFileSync(filePath, 'utf8');
      return { filename, filePath, content };
    });
  
  console.debug(`📁 找到 ${files.length} 个 Markdown 文件`);
  return files;
}

// 解析 Front Matter
function parseFrontMatter(content) {
  const fmRegex = /^(\uFEFF)?(?:---|\+\+\+)\r?\n([\s\S]*?)\r?\n(?:---|\+\+\+)(?:\s*?$)/m;
  const match = content.match(fmRegex);
  
  if (match) {
    try {
      const frontMatter = yaml.load(match[2]);
      const body = content.substring(match[0].length);
      return { frontMatter, body, raw: match[0] };
    } catch (e) {
      console.warn('YAML 解析错误:', e.message);
    }
  }
  
  return { frontMatter: {}, body: content, raw: '' };
}

// 自动判断分类
function autoDetectCategory(title, body) {
  const text = (title + ' ' + body).toLowerCase();
  
  for (const [keywords, category] of Object.entries(CATEGORY_MAP)) {
    if (text.includes(keywords.toLowerCase())) {
      return category;
    }
  }
  
  return '未分类';
}

// 自动生成描述（取第一段非空内容）
function autoGenerateDescription(body) {
  const lines = body.split('\n').filter(line => {
    const trimmed = line.trim();
    // 跳过空行、图片、标题
    return trimmed && 
           !trimmed.startsWith('![') && 
           !trimmed.startsWith('#') &&
           !trimmed.startsWith('---') &&
           trimmed.length > 20;
  });
  
  if (lines.length > 0) {
    let desc = lines[0].trim();
    // 移除 Markdown 格式
    desc = desc.replace(/\*\*/g, '').replace(/\*/g, '');
    desc = desc.replace(/`/g, '');
    // 截断到 100 字符
    if (desc.length > 100) {
      desc = desc.substring(0, 100) + '...';
    }
    return desc;
  }
  
  return '';
}

