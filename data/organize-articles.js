// 处理单个文件
function processFile(file) {
  const { filename, filePath, content } = file;
  const { frontMatter, body, raw } = parseFrontMatter(content);
  
  // 自动检测分类
  const category = frontMatter.category || autoDetectCategory(frontMatter.title || filename, body);
  
  // 自动生成描述
  const description = frontMatter.description || autoGenerateDescription(body);
  
  // 自动生成标签
  const tags = autoGenerateTags(frontMatter, body);
  
  // 更新 Front Matter
  const newFrontMatter = {
    title: frontMatter.title || filename.replace('.md', ''),
    description: description,
    category: category,
    tags: tags
  };
  
  // 重新组合内容
  const newRaw = '---\n' + yaml.dump(newFrontMatter, { indent: 2, lineWidth: -1 }) + '---\n';
  const newContent = newRaw + body;
  
  // 写入文件
  fs.writeFileSync(filePath, newContent, 'utf8');
  
  return {
    filename,
    title: newFrontMatter.title,
    category,
    tags,
    description
  };
}

// 统计分类
function countCategories(results) {
  const stats = {};
  results.forEach(r => {
    stats[r.category] = (stats[r.category] || 0) + 1;
  });
  return stats;
}

// 主函数
async function organize() {
  console.debug('🚀 开始整理知识库文章...\n');
  
  const files = readFiles();
  const results = [];
  
  for (const file of files) {
    try {
      const result = processFile(file);
      results.push(result);
      console.debug(`✅ ${result.filename}`);
      console.debug(`   分类: ${result.category}`);
      console.debug(`   标签: ${result.tags.join(', ')}`);
      console.debug(`   描述: ${result.description.substring(0, 50)}...`);
      console.debug('');
    } catch (e) {
      console.error(`❌ 处理失败 ${file.filename}:`, e.message);
    }
  }
  
  // 输出统计
  console.debug('\n📊 整理完成统计:');
  console.debug(`   总文章数: ${results.length}`);
  console.debug('\n   分类分布:');
  const stats = countCategories(results);
  Object.entries(stats)
    .sort((a, b) => b[1] - a[1])
    .forEach(([category, count]) => {
      console.debug(`   - ${category}: ${count}篇`);
    });
  
  console.debug('\n✨ 整理完成！现在可以运行 node data/build.js 生成数据');
}

// 运行
organize();
