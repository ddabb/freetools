  // 检查是否是自我关系（仅处理明确的自我关系）
  isSelfRelationship: function(chain) {
    if (chain.length < 2) return false;
    
    // 只处理明确的自我关系（配偶关系）- 使用口语化文字
    const selfRelationships = [
      ['老公', '老婆'], // 丈夫的妻子 = 自己
      ['老婆', '老公'], // 妻子的丈夫 = 自己
      // 兄弟姐妹关系不能确定是自我关系
      // 父母子女关系不能确定是自我关系
    ];
    
    // 检查整个关系链是否能简化为自我关系
    // 只有当关系链经过简化后为空时才返回 true
    let simplifiedChain = [...chain];
    let changed = true;
    
    while (changed && simplifiedChain.length >= 2) {
      changed = false;
      for (let i = 0; i < simplifiedChain.length - 1; i++) {
        const pair = [simplifiedChain[i], simplifiedChain[i + 1]];
        for (const selfPair of selfRelationships) {
          if (pair[0] === selfPair[0] && pair[1] === selfPair[1]) {
            // 移除这对自我关系
            simplifiedChain.splice(i, 2);
            changed = true;
            break;
          }
        }
        if (changed) break;
      }
    }
    
    // 只有当关系链完全被简化（为空）时才返回 true
    return simplifiedChain.length === 0;
  },
  
