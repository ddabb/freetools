/**
 * 获取指定分类的符号
 * @param {string} category - 分类名称
 * @returns {Array} 符号列表
 */
function getSymbolsByCategory(category) {
  return SPECIAL_SYMBOLS[category] || [];
}

/**
 * 获取所有分类及其符号
 * @returns {Object} 分类符号对象
 */
function getAllSymbols() {
  return SPECIAL_SYMBOLS;
}

/**
 * 获取分类名称映射
 * @returns {Object} 分类名称对象
 */
function getCategoryNames() {
  return CATEGORY_NAMES;
}

/**
 * 搜索符号
 * @param {string} keyword - 搜索关键词
 * @returns {Array} 匹配的符号列表
 */
function searchSymbols(keyword) {
  const results = [];
  Object.values(SPECIAL_SYMBOLS).forEach(symbols => {
    symbols.forEach(symbol => {
      if (symbol.includes(keyword)) {
        results.push(symbol);
      }
    });
  });
  return results;
}

